package Cursojava.executavel;

public class OperadoresRelacionais {
	public static void main(String[] args) {

		/*
		  == -> igual
		  != -> diferente
		   < -> menor que
		   > -> maior que
		  <= -> menor ou igual a
		  >= -> maior ou igual a
		 
		 */
		int nota1 = 90;
		int nota2 = 91;
		
		if(nota1 == nota2) {
			System.out.println("As notas são iguais");
		}else {
			System.out.println("As notas são diferentes");
		}
	
	}

}
